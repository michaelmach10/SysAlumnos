    private void txtUsuarioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUsuarioKeyPressed
        
        int key = evt.getKeyCode();
        if(key == KeyEvent.VK_ENTER){
            if(!txtUsuario.getText().equals("")){
                op = ud.verificarUsuario(txtUsuario.getText());
                if(op==1){
                    JOptionPane.showMessageDialog(null, "Usuario existe!");
                    txtUsuario.setText(null);
                    txtUsuario.requestFocus();
                }else{
                    JOptionPane.showMessageDialog(null, "Usuario no existe!");
                    txtClave.setEditable(true);
                    txtClave.requestFocus();
                }
            }
        }
    }//GEN-LAST:event_txtUsuarioKeyPressed

    private void txtClaveKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtClaveKeyPressed
        int key = evt.getKeyCode();
        if(key == KeyEvent.VK_ENTER){
            if(!txtUsuario.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Digitar Clave");                
            }else{
                btnRegistrar.setEnabled(true);
                btnRegistrar.requestFocus();
            }
        }
    }//GEN-LAST:event_txtClaveKeyPressed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String usuario = txtUsuario.getText();
        String clave = txtClave.getText();
        op = ud.registrarUsuario(usuario, clave);
        limpiar();
        if(op!=0){
            JOptionPane.showMessageDialog(null,"Usuario registrado!");
            updateComponets();
        }else{
            JOptionPane.showMessageDialog(null,"Usuario no registrado!");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tbUsuarios.getSelectedRow();
        if(fila<0){
            JOptionPane.showMessageDialog(null, "Seleccionar El Usuario a ELIMINAR");            
        }else{
            int confirmar=JOptionPane.showConfirmDialog(null, "¿Esta seguro que desea ELIMINAR a este Usuario? "); 
            if(JOptionPane.OK_OPTION==confirmar) {
                    int celda = (int) tbUsuarios.getValueAt(fila, 0);
                    int x = ud.eliminarUsuario(celda);
                    if(x==1){
                        JOptionPane.showMessageDialog(null, "Usuario ELIMINADO!");
                        updateComponets();
                        limpiar();
                    }else{
                        JOptionPane.showMessageDialog(null, "Usuario no ELIMINADO!");      
                    }                   
            } 
            
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int fil = tbUsuarios.getSelectedRow();
        if(fil<0){
            JOptionPane.showMessageDialog(null, "Seleccionar El Usuario a MODIFICAR!");
        }else{
            int confirmar=JOptionPane.showConfirmDialog(null, "Esta seguro que desea MODIFICAR el Usuario? "); 
            if(JOptionPane.OK_OPTION==confirmar) {
                  int id = Integer.parseInt(txtId.getText());
                  String user = txtUsuario.getText();
                  String clave = txtClave.getText();
                                    
                  int x = ud.modificarUsuario(id, user, clave);
                  if(x==1){
                      JOptionPane.showMessageDialog(null, "Usuario MODIFICADO!");
                      updateComponets();
                      limpiar();
                  }else{
                      JOptionPane.showMessageDialog(null, "El Usuario no se ha MODIFICADO!");      
                  }                   
            }        
        }
    }//GEN-LAST:event_btnModificarActionPerformed

