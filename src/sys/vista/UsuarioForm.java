    private void tbUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbUsuariosMouseClicked
        if(evt.getButton()==1){
            int fila = tbUsuarios.getSelectedRow();
            int celda = (int)tbUsuarios.getValueAt(fila, 0);
            lista = ud.listarUsuario(celda);
            for(int i=0;i<lista.size();i++){
                txtId.setText(""+lista.get(i).getId());
                txtUsuario.setText(lista.get(i).getUsuario());
                txtClave.setText(lista.get(i).getClave());
            }
        }
    }//GEN-LAST:event_tbUsuariosMouseClicked
    
    //listar datos en la tabla    
    void listar(){
    lista = ud.listarUsuario();
    model = (DefaultTableModel) tbUsuarios.getModel();
        Object[] u = new Object[5];
        for(int i=0;i<lista.size();i++){
            u[0]=lista.get(i).getId();
            u[1]=lista.get(i).getUsuario();
            u[2]=lista.get(i).getClave();
            model.addRow(u);
        }        
        tbUsuarios.setModel(model);
    }
    
    //limpiar los elemento para un nuevo ingreso
    void limpiar(){
        txtId.setText(null);
        txtUsuario.setText(null);
        txtClave.setText(null);
    }
    
    //
    void updateComponets(){
            LimpiarTabla(model);
            listar();
            modelocombo.removeAllElements();
            modelolista.clear();    
    }
   
    void LimpiarTabla(DefaultTableModel modelo){
        int a =modelo.getRowCount()-1;
        for(int i=a;i>=0;i--){  
            modelo.removeRow(i);
        }
    }

    
